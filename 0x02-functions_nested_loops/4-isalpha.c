#include "main.h"
#include <ctype.h>

/**
 * _isalpha - A function that checks if an argument is alphabetic.
 * @c: the argument to check if its alphabetic.
 * Return: 1 if the argument is alphabetic and 0 if it's not.
 */

int _isalpha(int c);
{
if (isalpha(c))
{
return (1);
}
else
{
return (0);
}
}
